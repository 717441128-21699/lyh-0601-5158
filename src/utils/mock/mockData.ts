const generateHourlyData = (baseVolume: number, basePressure: number): DredgeHourData[] => {
  return Array.from({ length: 24 }, (_, i) => ({
    hour: i,
    volume: Math.max(0, baseVolume + Math.sin(i / 4) * 15 + (Math.random() - 0.5) * 10),
    pressure: Math.max(0.8, basePressure + Math.sin(i / 3) * 0.3 + (Math.random() - 0.5) * 0.2),
  }));
};

