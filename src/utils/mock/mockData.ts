export const generateMockForecast = (): ForecastDay[] => {
  const today = new Date();
  const days: ForecastDay[] = [];
  const weather: ForecastDay['weatherType'][] = ['晴', '多云', '晴', '小雨', '多云'];
  for (let i = 0; i < 5; i++) {
    const d = new Date(today);
    d.setDate(today.getDate() + i);
    const dateStr = `${d.getMonth() + 1}/${d.getDate()}`;
    const baseV = 1200 + Math.sin(i) * 200;
    const histV = 1100 + Math.sin(i + 0.5) * 180;
    const factor = weather[i] === '大雨' ? 0.7 : weather[i] === '小雨' ? 0.85 : weather[i] === '多云' ? 0.95 : 1.0;
    const tLow = 18 + (Math.random() * 4);
    const tHigh = 26 + (Math.random() * 4);
    days.push({
      date: dateStr,
      forecastVolume: Math.round(baseV * factor),
      historicalVolume: Math.round(histV),
      weatherFactor: factor,
      weatherType: weather[i],
      temperature: [Math.round(tLow), Math.round(tHigh)],
    });
  }
  return days;
};

